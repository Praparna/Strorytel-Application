BEGIN TRANSACTION;
CREATE TABLE IF NOT EXISTS "USER" (
	"USER_ID"	INTEGER,
	"USER_NAME"	TEXT,
	"USER_EMAIL"	TEXT,
	"USER_MOBILE"	INTEGER,
	"SUBSCRIPTION_PLAN"	INTEGER,
	"COUNTRY_ID"	INTEGER,
	FOREIGN KEY("COUNTRY_ID") REFERENCES "COUNTRY"("COUNTRY_ID"),
	PRIMARY KEY("USER_ID")
);
CREATE TABLE IF NOT EXISTS "COUNTRY" (
	"COUNTRY_ID"	INTEGER,
	"COUNTRY"	TEXT,
	PRIMARY KEY("COUNTRY_ID")
);
CREATE TABLE IF NOT EXISTS "LOGIN" (
	"LOGIN_ID"	INTEGER,
	"USER_ID"	INTEGER,
	"PASSWORD"	TEXT,
	"PASSCODE"	INTEGER,
	"KIDSMODE"	TEXT,
	FOREIGN KEY("USER_ID") REFERENCES "USER"("USER_ID"),
	PRIMARY KEY("LOGIN_ID")
);
CREATE TABLE IF NOT EXISTS "BOOKS" (
	"BOOK_ID"	INTEGER,
	"BOOK_RATING"	REAL,
	"TRACK_RATING"	INTEGER,
	"DURATION(INHOURS)"	TEXT,
	"PUBLICATION_YEAR"	INTEGER,
	"RECOMMENDATION"	TEXT,
	"BOOK_NAME"	TEXT,
	PRIMARY KEY("BOOK_ID")
);
CREATE TABLE IF NOT EXISTS "BOOK_GENRE" (
	"BOOK_ID"	INTEGER,
	"GENRE_ID"	INTEGER,
	FOREIGN KEY("GENRE_ID") REFERENCES "GENRE"("GENRE_ID"),
	FOREIGN KEY("BOOK_ID") REFERENCES "BOOKS"("BOOK_ID"),
	PRIMARY KEY("BOOK_ID","GENRE_ID")
);
CREATE TABLE IF NOT EXISTS "GENRE" (
	"GENRE_ID"	INTEGER,
	"GENRE"	TEXT,
	PRIMARY KEY("GENRE_ID")
);
CREATE TABLE IF NOT EXISTS "BOOK_AUTHORSHIP" (
	"AUTHORSHIP_ID"	INTEGER,
	"BOOK_ID"	INTEGER,
	"AUTHOR_ID"	INTEGER,
	FOREIGN KEY("BOOK_ID") REFERENCES "BOOKS"("BOOK_ID"),
	FOREIGN KEY("AUTHOR_ID") REFERENCES "AUTHOR"("AUTHOR_ID"),
	PRIMARY KEY("AUTHORSHIP_ID","BOOK_ID","AUTHOR_ID")
);
CREATE TABLE IF NOT EXISTS "AUTHOR" (
	"AUTHOR_ID"	INTEGER,
	"AUTHOR_FIRST_NAME"	TEXT,
	"AUTHOR_LAST_NAME"	TEXT,
	"DEBUT_YEAR"	INTEGER,
	"NUM_BOOKS"	INTEGER,
	"LANGUAGE_ID"	INTEGER,
	FOREIGN KEY("LANGUAGE_ID") REFERENCES "VARIETY"("LANGUAGE_ID"),
	PRIMARY KEY("AUTHOR_ID")
);
CREATE TABLE IF NOT EXISTS "VARIETY" (
	"BOOK_ID"	INTEGER,
	"LANGUAGE_ID"	INTEGER,
	"AUTHOR_ID"	INTEGER,
	FOREIGN KEY("BOOK_ID") REFERENCES "BOOKS"("BOOK_ID"),
	FOREIGN KEY("LANGUAGE_ID") REFERENCES "LANGUAGE"("LANGUAGE_ID"),
	FOREIGN KEY("AUTHOR_ID") REFERENCES "AUTHOR"("AUTHOR_ID"),
	PRIMARY KEY("BOOK_ID","LANGUAGE_ID","AUTHOR_ID")
);
CREATE TABLE IF NOT EXISTS "LANGUAGE" (
	"LANGUAGE_ID"	INTEGER,
	"PRIMARY_LANGUAGE"	TEXT,
	"SECONDARY_LANGUAGE"	TEXT,
	PRIMARY KEY("LANGUAGE_ID")
);
CREATE TABLE IF NOT EXISTS "BOOKSHELF" (
	"BOOK_ID"	INTEGER,
	"USER_ID"	INTEGER,
	"READING_STATUS"	TEXT,
	"DURATION_COMPLETED"	TEXT,
	"LIKED_BOOK"	TEXT,
	"BOOK_MARK"	INTEGER,
	FOREIGN KEY("BOOK_ID") REFERENCES "BOOKS"("BOOK_ID"),
	FOREIGN KEY("USER_ID") REFERENCES "INSTAGRAM"("INSTAGRAM_ID"),
	PRIMARY KEY("BOOK_ID","USER_ID")
);
CREATE TABLE IF NOT EXISTS "INSTAGRAM" (
	"INSTAGRAM_ID"	TEXT,
	"USER_ID"	INTEGER,
	"BOOKID"	INTEGER,
	FOREIGN KEY("USER_ID") REFERENCES "USER"("USER_ID"),
	PRIMARY KEY("INSTAGRAM_ID")
);
CREATE TABLE IF NOT EXISTS "FRIEND" (
	"USER_ID"	TEXT,
	"EMAIL_ID_FRIEND"	TEXT,
	"FRIEND_NAME"	TEXT,
	FOREIGN KEY("USER_ID") REFERENCES "USER"("USER_ID")
);
CREATE TABLE IF NOT EXISTS "SERIES" (
	"SERIES_ID"	INTEGER,
	"STORIES"	TEXT,
	"NUMBER_OF_EPISODES"	INTEGER,
	"DURATION"	TEXT,
	"EPISODES_NO_1"	TEXT,
	"EPISODES_NO_2"	TEXT,
	"EPISODES_NO_3"	TEXT,
	"EPISODES_NO_4"	TEXT,
	"EPISODES_NO_5"	TEXT,
	PRIMARY KEY("SERIES_ID")
);
CREATE TABLE IF NOT EXISTS "USER_LANGUAGES" (
	"USER_ID"	INTEGER,
	"LANGUAGE_ID"	INTEGER,
	"SKILL_LEVEL"	TEXT,
	FOREIGN KEY("USER_ID") REFERENCES "USER"("USER_ID"),
	FOREIGN KEY("LANGUAGE_ID") REFERENCES "LANGUAGE"("LANGUAGE_ID"),
	PRIMARY KEY("USER_ID","LANGUAGE_ID")
);
